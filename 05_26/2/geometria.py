def area_quadrado(lado):
    return lado * lado
def area_retangulo(base, altura):
    return base * altura
def area_circulo(raio):
    pi = 3.14
    return pi * raio * raio
def perimetro_circulo(raio):
    pi = 3.14
    return 2 * pi * raio
def volume_cilindro(raio, altura):
    pi = 3.14
    return pi * raio * raio * altura